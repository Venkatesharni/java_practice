package problem_statement3;
/**
 * This program demonstrates While loop working and with the help of while we are going to 
 * print 1 to 9 numbers
 */

public class while_loop {
	public static void main(String[] args) {
		int x=1;//instalization of variable
		System.out.print("The numbers are.......");
		//condition
		while(x<10) {
			
			System.out.print(x);
			x++;// increment operation
				
		}
	}

}
